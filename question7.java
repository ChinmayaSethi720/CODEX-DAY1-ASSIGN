
public class question7 
{
	public static void main(String[] args) 
	{
		char n='*';
		float a=10, b=7, c=0;
		switch (n)
		{
		case '+':
			c=a+b;
				System.out.println("The value of a+b= "+c);
			break;
		case '-':
			c=a-b;
				System.out.println("The value of a-b= "+c);
			break;
		case '*':
			c=a*b;
				System.out.println("The value of a*b= "+c);
			break;
		case '/':
			c=a/b;
				System.out.println("The value of a/b= "+c);
			break;
		default:
			System.out.println("Error!");
			
			
		
		}
}
}
